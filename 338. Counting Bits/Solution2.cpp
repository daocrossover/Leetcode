// i & (i-1): judge whether num is the pow of 2
// i    binary '1'  i&(i-1)
// 0    0000    0
// -----------------------
// 1    0001    1    0000
// -----------------------
// 2    0010    1    0000
// 3    0011    2    0010
// -----------------------
// 4    0100    1    0000
// 5    0101    2    0100
// 6    0110    2    0100
// 7    0111    3    0110
// -----------------------
// 8    1000    1    0000
// 9    1001    2    1000
// 10   1010    2    1000
// 11   1011    3    1010
// 12   1100    2    1000
// 13   1101    3    1100
// 14   1110    3    1100
// 15   1111    4    1110

#include<vector>
using namespace std;

class Solution {
public:
    vector<int> countBits(int num) {
        vector<int> dp(num + 1);
        for (int i = 1; i <= num; ++i) {
            dp[i] = dp[i & (i-1)] + 1;
        }
        return dp;
    }
};